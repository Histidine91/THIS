local moveDefs = {
	HOVER6 = {
		footprintx = 6,
		footprintz = 6,
		maxwaterdepth = 9000,
		maxslope = 9000,
		crushstrength = 0,
	},
}

--------------------------------------------------------------------------------
--------------------------------------------------------------------------------

-- convert from map format to the expected array format

local array = {}
local i = 1
for k,v in pairs(moveDefs) do
	v.heatmapping = false -- disable heatmapping
	array[i] = v
	v.name = k
	i = i + 1
end


--------------------------------------------------------------------------------
--------------------------------------------------------------------------------

return array

